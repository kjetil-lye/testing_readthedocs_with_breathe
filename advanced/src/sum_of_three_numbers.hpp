//! Computes the sum of three numbers
//!
//! @param a the first operand
//! @param b the second operand
//! @param c the second operand
//! @return \f$a + b + c\f$
//!
//! @note this is quite notable.
template<class T>
T sum_of_three_numbers(const T& a, const T& b, const T& c) {
  return a + b + c;
}
